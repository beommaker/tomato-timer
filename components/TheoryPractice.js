function greet(lang){
    if(lang === 'spanish'){
        return 'hola';
    } else if(lang === 'korean'){
        return '안녕하세요';
    } else if(lang === 'greek'){
        return 'giasue';
    } else if(lang === 'italian'){
        return 'chao';
    } else if(lang === 'english'){
        return 'hello!';
    } else {
        return 'dont know that one';
    }
}
